#!/usr/bin/env bash
# Воспроизводит историю разговора с SkyHelper через curl.
# Запуск: bash replay.sh
# Сервер должен быть запущен на localhost:8000 с gateway на 8001.

BASE="http://localhost:8000/chat"
SESSION="replay-$(date +%s)"
USER="DEMO_USER"

send() {
  local label="$1"
  local msg="$2"
  echo ""
  echo "━━━ $label ━━━"
  echo "→ $msg"
  local body
  body=$(python3 -c "import json,sys; print(json.dumps({'session_id':'$SESSION','message':sys.argv[1],'use_gateway':True}))" "$msg")
  curl -s -X POST "$BASE" \
    -H "Content-Type: application/json" \
    -H "X-User-Id: $USER" \
    -d "$body" \
    | python3 -c '
import json, sys
d = json.load(sys.stdin)
for c in d.get("tool_calls", []):
    print("  🔧", c["name"], "→", c["result"][:100])
print("←", d.get("reply", "???"))
alerts = d.get("guard_alerts", [])
if alerts:
    print("  ⚠️  guard:", alerts)
'
  sleep 1
}

